print("Get the data type with the Type() function")

x = "RJ"
print(type(x))

y = 5.0
print(type(y))

House = ["Car", "Stairs", "Dog"]
print(type(House))

classRoomSupplies  = ("Pencil", "Eraser", "Notebook")
print(type(classRoomSupplies))

print("You can also set a specific data type")

x = str("Hello World")
print(type(x))

y = int(20)
print(type(y))

z = float(5.5)
print(type(z))